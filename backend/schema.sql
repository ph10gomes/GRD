  participante VARCHAR(150) NOT NULL,
  funcao VARCHAR(80) NOT NULL,
  us_programada_wm DECIMAL(10,2) NULL,
  us_realizada_wm DECIMAL(10,2) NULL,
  desvio_wm DECIMAL(10,2) NULL,
  us_programada_lt DECIMAL(10,2) NULL,
  us_realizada_lt DECIMAL(10,2) NULL,
  desvio_lt DECIMAL(10,2) NULL,
  complemento_lancado VARCHAR(255) NULL,
  mf_us_dia DECIMAL(10,2) NULL,
  mt_us_dia DECIMAL(10,2) NULL,
  equipes_d_moto INT NULL,
  equipes_d_equipe INT NULL,
  equipe_orcada_d1 INT NULL,
  equipe_total_d1 INT NULL,
  equipe_programada_d1 INT NULL,
  equipe_efetiva_d1 INT NULL,
  plataforma_qtd_eqp INT NULL,
  plataforma_hora_liberada VARCHAR(50) NULL,
  almoxarifado_qtd_eqp INT NULL,
  almoxarifado_hora_liberacao VARCHAR(50) NULL,
  absenteismo_atestado_qtd INT NULL,
  previsao_retorno_atestado_d1 VARCHAR(100) NULL,
  absenteismo_falta_qtd INT NULL,
  motivo_falta_d1 VARCHAR(255) NULL,
  oficina_qtd_d1 INT NULL,
  oficina_hora_liberacao_d1 VARCHAR(50) NULL,
  motivo_perda_1 VARCHAR(255) NULL,
  motivo_perda_2 VARCHAR(255) NULL,
  motivo_perda_3 VARCHAR(255) NULL,
