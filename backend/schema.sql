  foco_prioridade_1 VARCHAR(255) NULL,
  foco_prioridade_2 VARCHAR(255) NULL,
  foco_prioridade_3 VARCHAR(255) NULL,
  foco_prioridade_4 VARCHAR(255) NULL,
  foco_prioridade_5 VARCHAR(255) NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id)
);
